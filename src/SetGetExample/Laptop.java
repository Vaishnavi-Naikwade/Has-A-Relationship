package SetGetExample;

public class Laptop
{
	private int lapId;
	private String lapName;
	private String lapColor;
	private float lapPrice;
	
	public int getLapId() {
		return lapId;
	}
	public void setLapId(int lapId) {
		this.lapId = lapId;
	}
	public String getLapName() {
		return lapName;
	}
	public void setLapName(String lapName) {
		this.lapName = lapName;
	}
	public String getLapColor() {
		return lapColor;
	}
	public void setLapColor(String lapColor) {
		this.lapColor = lapColor;
	}
	public float getLapPrice() {
		return lapPrice;
	}
	public void setLapPrice(float lapPrice) {
		this.lapPrice = lapPrice;
	}
	
	
}
